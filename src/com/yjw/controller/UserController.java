package com.yjw.controller;
import javax.servlet.http.HttpSession;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.ResponseBody;

import com.yjw.po.User;
import com.yjw.service.UserService;
/**
 * 用户控制器类
 */
@Controller
public class UserController {
	// 依赖注入
	@Autowired
	private UserService userService;
	/**
	 * 用户登录
	 */
	@RequestMapping(value = "/login", method = RequestMethod.POST)
	public String login(String usercode,String password, Model model,  HttpSession session) {
		// 通过账号和密码查询用户
		User user = userService.findUser(usercode, password);
		if(user != null){		
			// 将用户对象添加到Session
			session.setAttribute("User", user);
			// 跳转到主页面
			return "home";
		}
		model.addAttribute("msg", "账号或密码错误，请重新输入！");
         // 返回到登录页面
		return "login";
	}
	
	/**
	 * 退出登录
	 */
	@RequestMapping("/logout")
	public String logout(HttpSession session) {
	    // 清除Session
	    session.invalidate();
	    // 重定向到登录页面的跳转方法
	    return "redirect:/login.action";
	}
	/**
	 * 向用户登陆页面跳转
	 */
	@RequestMapping(value = "/login.action", method = RequestMethod.GET)
	public String toLogin() {
	    return "login";
	}
	
	/**
	 * 修改信息
	 * @param user
	 * @param session
	 * @return
	 */
	@RequestMapping("/returnXg.action")
	@ResponseBody
	public String returnXg(User user,HttpSession session) {
		
		System.out.println(user);
		int i=userService.setUser(user);
		if(i>0) {
			return "OK";
		}else {
			return "NO";
		}
	}
	/**
	 * 获得id
	 * @param id
	 * @return
	 */
	@RequestMapping("/userId.action")
	@ResponseBody
	public User userId(Integer id) {
		System.out.println("User"+id);
		User user = userService.getID(id);
	    return user;
	}
	

}
